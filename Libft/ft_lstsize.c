#include "libft.h"

int			ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*temp;

	if (lst)
	{
		i = 1;
		temp = lst;
		while (temp->next)
		{
			temp = temp->next;
			i++;
		}
		return (i);
	}
	else
		return (0);
}
